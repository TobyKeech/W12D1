
    public class Calculator {

        public Calculator(){};

        public int add(int a, int b){
           int total = a + b;
           return total;
        };

        public int subtract(int a, int b){
            int total = a - b;
            return total;
        }

        public int multiply( int a, int b){
            int total = a * b;
            return total;
        }

        public double divide (double a, double b){
            double total = a / b;
            return total;
        }
    }


